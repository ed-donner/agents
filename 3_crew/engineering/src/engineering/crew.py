from crewai import Agent, Crew, Process, Task
from crewai.project import CrewBase, agent, crew, task
from crewai.agents.agent_builder.base_agent import BaseAgent


@CrewBase
class Engineering():
    """Engineering crew"""

    agents: list[BaseAgent]
    tasks: list[Task]

    @agent
    def engineering_lead(self) -> Agent:
        return Agent(
            config=self.agents_config['engineering_lead'], 
            verbose=True
        )

    @agent
    def backend_engineer(self) -> Agent:
        return Agent(
            config=self.agents_config['backend_engineer'], 
            verbose=True,
            # Code execution requires Docker on CrewAI; disable it for environments
            # where Docker isn't installed.
            allow_code_execution=False,
            code_execution_mode="safe",  
            max_execution_time=240, 
            max_retry_limit=5
        )
    
    @agent
    def frontend_engineer(self) -> Agent:
        return Agent(
            config=self.agents_config['frontend_engineer'],
            verbose=True,
                   )
    
    @agent
    def test_engineer(self) -> Agent:       
        return Agent(
            config=self.agents_config['test_engineer'],
            verbose=True,
            # Unit tests are generated by the LLM; Docker-based execution isn't needed here.
            allow_code_execution=False,
            code_execution_mode="safe",
            max_execution_time=240,
            max_retry_limit=5
        )
    
    @task
    def design_task(self) -> Task:
        return Task(
            config=self.tasks_config['design_task']
            
        )
    
    @task
    def code_task(self) -> Task:
        return Task(
            config=self.tasks_config['code_task']
          
        )
    
    @task
    def frontend_task(self) -> Task:
        return Task(
            config=self.tasks_config['frontend_task']
          
        )
    
    @task
    def test_task(self) -> Task:
        return Task(
            config=self.tasks_config['test_task']
          
        )


    @crew
    def engineering_crew(self) -> Crew:
        return Crew(
            agents=self.agents,
            tasks=self.tasks,
            process=Process.sequential,
            verbose=True
        )